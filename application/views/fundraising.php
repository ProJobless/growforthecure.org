	<p class="question">Personalize.</p>
	<p class="answer">You can upload photos, change grow styles, send team invites and announce your grow on Facebook or Twitter. Also, take a minute to add a personal statement about why fighting lung cancer is important to you.</p>

	<p class="question">Treat yourself.</p>
	<p class="answer">Kick off your grow by making the first donation to yourself. What a great way to show your commitment and be on your way to fighting lung cancer.</p>


	<p class="question">Get Social.</p>
	<p class="answer">Facebook and Twitter can be your biggest advertisers. Announce your grow and keep giving updates and posting pictures along the way. Email is another easy way to ask for donations--edit and forward on your welcome email.</p>

	<p class="question">Party On.</p>
	<p class="answer">Throw a kick off party. You could even have guests RSVP by making a donation.</p>

	<p class="question">Ask.</p>
	<p class="answer">People are more receptive than you think. Donors want to see their friend succeed. In fact, many will donate again if they know you are close to your goal. Your inner circle of friends, family and fans will be a great source of initial support. Use this to create public validation for your grow, as well as an army of spokespeople to help spread your campaign on social networks.<br /><br />
	Ask your company if they will gift match, itâ€™s an easy way to double your efforts.<br /><br />
	Ask a business that you support to support you - your local bar, coffee shop, restaurant, doctor, hairstylist, etc.</p>

	<p class="question">Get Creative.</p>
	<p class="answer">Post your fundraising link on a tear-away flyer and hang it in the break room at work with a picture connected to the cause. Your co-workers can tear off your web address and check out your campaign.<br /><br />
	Place your fundraising link in the signature line of your email. With no extra effort, every email you send is another opportunity to fundraise and spread the word about lung cancer.</p>

	<p class="question">Educate.</p>
	<p class="answer">Educate your friends and family on how anyone can get lung cancer and stress the benefits of raising money to fight it. Refer to the lung cancer facts and our partner website, lungcancerfoundation.org, for more information.</p>

	<p class="question">Be Thankful.</p>
	<p class="answer">Grow for the Cure sends out thank you emails when a donation is made, but it is always nice to back that up with a private message.</p>




