		<p class="answer">Click on Become a Grower and choose whether you’d like to register as an individual, join a team or start a team. Some general information will be needed for registration - complete all fields with an asterisk.</p>

		<p class="question">I forgot my password.</p>
		<p class="answer">Under the login function, there is an option for ‘forgot my password?’ Simply enter your email address and click ‘submit’. You will receive an automated email from Grow for the Cure, which will allow you to reset your password. If you don’t receive this email, please contact us at <a href="mailto:webmaster@growforthecure.org">webmaster@growforthecure.org</a> and we can investigate further.</p>

		<p class="question">Can I close and delete my profile page?</p>
